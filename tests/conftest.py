def make_zip(filename: str, body: bytes) -> bytes:
    """Pack one CSV into a NEMWEB-style ZIP and return the bytes."""
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, mode="w", compression=zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(filename, body)
    return buf.getvalue()


